public class StringExercise {
    public static void main(String[] args) {
        String floyd = "We don't need no education\nWe don't need no thought control";
        System.out.println(floyd);

        String quote = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quote);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String slash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(slash);
    }
}
